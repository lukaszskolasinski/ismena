        <h3>Witamy w sklepie Ismena</h3>
        <p>Siedziba firmy znajduje się w Nowej Rudzie, mieście w południowo-zachodniej części Polski, leżącej w Sudetach Środkowych, u podnóża Gór Sowich.</p> 

        <p>Od początku działalności kierowaliśmy się zdrowiem i dobrą kondycją roślin, a także starannym przygotowaniem ich do transportu.  

            Byliny kupujemy w formie siewek i malutkich sadzonek. 

            Nie używamy tuneli ogrodowych, "nie pędzimy roślin"

            Byliny w doniczkach na okres zimy pozostawiane są na zewnątrz, dzięki czemu, bez problemów aklimatyzują się w nowych warunkach.
        </p>  

        <p>Każda roślina zaopatrzona jest w paszport, zgodnie z rozporządzeniem wynikających z dwóch rozporządzeń Parlamentu Europejskiego i Rady Unii Europejskiej.

            Nadzór nad uprawami naszych roślin sprawuje Państwowa Inspekcja Ochrony Roślin i Nasiennictwa we Wrocławiu, Oddział w Kłodzku.
        </p>

        <p>Z pełną satysfakcją naszych klientów, można zapoznać się czytając komentarze w serwisie Allegro, klikając na odnośnik, który znajduje się poniżej, po prawej stronie

            ekranu.
        </p>

        <p>W związku z licznymi pytaniami, uprzejmie informujemy, iż prowadzimy sprzedaż wyłącznie wysyłkową, w związku z czym <b>nie ma możliwości osobistego 
            zakupu, ani odbioru roślin.</b>
        </p>


        <p>Życzymy udanych zakupów. Dołożymy wszelkich starań, aby Państwo również dołączyli do rzeszy zadowolonych klientów.</p>

